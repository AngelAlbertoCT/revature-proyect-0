package com.projecto_0.models;

public class LoanTypeModel {
    private int id;
    private String name;

    public LoanTypeModel() {}

    public LoanTypeModel(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}